let currentCategoryType = null;

function showCategories(type, event) {
    if (event) event.preventDefault();
    
    const mainCategories = document.getElementById('mainCategories');
    const cablesCategories = document.getElementById('cablesCategories');
    const accessoriesCategories = document.getElementById('accessoriesCategories');
    const backToMainButton = document.getElementById('backToMain');
    const backToCategoriesButton = document.getElementById('backToCategories');
    const productsList = document.getElementById('productsList');
    
    currentCategoryType = type;
    
    mainCategories.style.display = 'none';
    backToMainButton.style.display = 'block';
    backToCategoriesButton.style.display = 'none';
    productsList.style.display = 'none';
    
    if (type === 'cables') {
        cablesCategories.style.display = 'grid';
        accessoriesCategories.style.display = 'none';
    } else if (type === 'accessories') {
        cablesCategories.style.display = 'none';
        accessoriesCategories.style.display = 'grid';
    }
    
    // Прокрутка к верху секции продуктов
    scrollToProductsSection();
}

function showProducts(category, event) {
    if (event) event.preventDefault();
    
    const mainCategories = document.getElementById('mainCategories');
    const cablesCategories = document.getElementById('cablesCategories');
    const accessoriesCategories = document.getElementById('accessoriesCategories');
    const productsList = document.getElementById('productsList');
    const backToMainButton = document.getElementById('backToMain');
    const backToCategoriesButton = document.getElementById('backToCategories');
    
    mainCategories.style.display = 'none';
    cablesCategories.style.display = 'none';
    accessoriesCategories.style.display = 'none';
    productsList.style.display = 'block';
    backToMainButton.style.display = 'none';
    backToCategoriesButton.style.display = 'block';
    
    productsList.innerHTML = `<h2>${category}</h2>`;
    
    if (products[category]) {
        products[category].forEach(product => {
            const productItem = document.createElement('div');
            productItem.className = 'product-item';
            productItem.textContent = product;
            productsList.appendChild(productItem);
        });
    } else {
        productsList.innerHTML += '<p>Товары для этой категории пока не добавлены.</p>';
    }
    
    // Прокрутка к верху секции продуктов
    scrollToProductsSection();
}

function showMainCategories(event) {
    if (event) event.preventDefault();
    
    const mainCategories = document.getElementById('mainCategories');
    const cablesCategories = document.getElementById('cablesCategories');
    const accessoriesCategories = document.getElementById('accessoriesCategories');
    const productsList = document.getElementById('productsList');
    const backToMainButton = document.getElementById('backToMain');
    const backToCategoriesButton = document.getElementById('backToCategories');
    
    mainCategories.style.display = 'grid';
    cablesCategories.style.display = 'none';
    accessoriesCategories.style.display = 'none';
    productsList.style.display = 'none';
    backToMainButton.style.display = 'none';
    backToCategoriesButton.style.display = 'none';
    
    currentCategoryType = null;
    
    // Прокрутка к верху секции продуктов
    scrollToProductsSection();
}

function backToCategories(event) {
    if (event) event.preventDefault();
    
    if (currentCategoryType) {
        showCategories(currentCategoryType);
    } else {
        showMainCategories();
    }
}

// Функция для плавной прокрутки к секции продуктов
function scrollToProductsSection() {
    const productsSection = document.getElementById('products');
    const headerHeight = document.querySelector('header').offsetHeight;
    const offsetTop = productsSection.offsetTop - headerHeight - 20;
    
    window.scrollTo({
        top: offsetTop,
        behavior: 'smooth'
    });
}

// Назначаем обработчики для кнопок "Назад"
document.getElementById('backToMain').addEventListener('click', showMainCategories);
document.getElementById('backToCategories').addEventListener('click', backToCategories);

// Обновляем вызовы функций в HTML
function updateClickHandlers() {
    // Основные категории
    const mainCategoryCards = document.querySelectorAll('.main-category-card');
    mainCategoryCards.forEach(card => {
        const currentOnclick = card.getAttribute('onclick');
        if (currentOnclick) {
            const newOnclick = currentOnclick.replace('showCategories', 'showCategoriesWithEvent');
            card.setAttribute('onclick', newOnclick);
        }
    });
    
    // Категории кабелей и аксессуаров
    const categoryCards = document.querySelectorAll('.category-card');
    categoryCards.forEach(card => {
        const currentOnclick = card.getAttribute('onclick');
        if (currentOnclick) {
            const newOnclick = currentOnclick.replace('showProducts', 'showProductsWithEvent');
            card.setAttribute('onclick', newOnclick);
        }
    });
}

// Вспомогательные функции для передачи event
function showCategoriesWithEvent(type) {
    showCategories(type, event);
}

function showProductsWithEvent(category) {
    showProducts(category, event);
}

// Инициализация после загрузки DOM
document.addEventListener('DOMContentLoaded', function() {
    updateClickHandlers();
    
    // Скрываем кнопки "Назад" при загрузке
    document.getElementById('backToMain').style.display = 'none';
    document.getElementById('backToCategories').style.display = 'none';
});
